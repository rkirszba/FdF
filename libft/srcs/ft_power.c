int	ft_power(int nb, int pwr)
{
	if (pwr < 0)
		return (-2);
	if (pwr == 0)
		return (1);
	else if (pwr == 1)
		return (nb);
	else
		return (nb * ft_power(nb, pwr - 1));
}
